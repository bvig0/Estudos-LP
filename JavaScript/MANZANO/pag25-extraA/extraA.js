let a = parseInt(prompt("Digite um valor (A): "))
let b = parseInt(prompt("Digite mais um valor (B): "))
let c = parseInt(prompt("Digite outro valor (C): "))
let d = parseInt(prompt("Digite outro valor (D): "))
let produto = a * c
let soma = b + d

alert("Produto de A e C (P): " + produto +
     "\nSoma de B e D (S): " + soma)